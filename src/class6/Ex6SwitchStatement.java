package class6;

public class Ex6SwitchStatement {
    public static void main(String[] args) {

        String name = "Vlad";

        switch(name){
            case "Vlad":
                System.out.println("Algonquin");
                break;
            case "Jacob":
                System.out.println("Chicago");
                break;
            case "Alex":
                System.out.println("IL");
                break;
        }
    }
}
