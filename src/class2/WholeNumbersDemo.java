package class2;

public class WholeNumbersDemo {

    public static void main(String[] args) {
        byte age = 50;
        System.out.println(age);

        short noOfApples = 20;
        System.out.println(noOfApples);

        //we use int almost all the time
        int bankBalance = 1000000;
        System.out.println(bankBalance);

        //if it is a large number we use "L" in the end
        long veryBigNumber = 454_554_545_45L; //45454545454l is also fine;
        System.out.println(veryBigNumber);


    }
}
