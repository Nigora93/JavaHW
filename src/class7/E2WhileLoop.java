package class7;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number = 0;
        //below we are using the increment operator(++ adds 1 to the actual value)
        number++;
        ++number;
        System.out.println(number);

        int num2 = 10;
        num2--;
        System.out.println(num2);

    }
}
